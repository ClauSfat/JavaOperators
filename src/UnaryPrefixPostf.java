public class UnaryPrefixPostf
{
    public static void main(String[] args) {
        int num = 1;
        System.out.println(num++);//varianta postfixata
        System.out.println(num);
        System.out.println(++num);//varianta prefixata


        System.out.println(num--);//varianta postfixata
        //System.out.println(num);
        System.out.println(--num);//varianta prefixata


    }
}
